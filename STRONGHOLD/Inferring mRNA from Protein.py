# количество кодонов, кодирующих аминокислоту
COUNT = {"F": 2, "L": 6, "I": 3, "V": 4, "M": 1,
         "S": 6, "P": 4, "T": 4, "A": 4, "Y": 2,
         "H": 2, "N": 2, "D": 2, "Q": 2, "K": 2,
         "E": 2, "C": 2, "R": 6, "W": 1, "G": 4}

# константа, на которую мы будем делить
MOD = 10 ** 6
a = input()
# сразу добавляем 3 стоп-кодона в ответ
ans = 3

# считаем, домножая и считая остаток
for i in a:
    ans *= COUNT[i]
    ans %= MOD

print(ans)
